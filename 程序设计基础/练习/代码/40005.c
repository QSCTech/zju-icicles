int main(void)
{
    int count, i, m, n, sum;
    int looptimes, ri;
    int isprime(int n);

    looptimes = GetInteger();
    for (ri = 1; ri <= looptimes; ++ri) {
        m = GetInteger();
        n = GetInteger();
        count = 0;
        sum = 0;
        for (i = m; i <= n; ++i) {
            if (isprime(i)) {
                ++count;
                sum += i;
            }
        }
        printf("Count = %d, sum = %d\n", count, sum);
    }
    return 0;
}

