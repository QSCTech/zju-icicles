int main(void)
{
    int count, i, j, k, m, n;
    
    int isprime(int n);

    m = GetInteger();
    n = GetInteger();
    if (m % 2 != 0) {
        m += 1;
    }
    count = 0;
    for (i = m; i <= n; i += 2) {
        k = i / 2;
        for (j = 2; j <= k; ++j) {
            if (isprime(j) && isprime(i - j)) {
                if (count == 5) {
                    printf("\n");
                    count = 0;
                }
                if (count != 0) {
                    printf(" ");
                }
                printf("%d=%d+%d", i, j, i - j);
                ++count;
                break;
            }
        }
    }
    printf("\n");
    return 0;
}

